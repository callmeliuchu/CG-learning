def generate(arr,t):
    if len(arr) == 1:
        return arr[0]
    ret = []
    for i in range(1,len(arr)):
        mid = arr[i-1] + (arr[i] - arr[i-1])*t
        ret.append(mid)
    return generate(ret,t)


arr = [Vector2f(x,y) for x,y in points]
xx = []
yy = []

for t in range(200):
    p = generate(arr,t/200.0)
    xx.append(p.x)
    yy.append(p.y)


xx1 = [v[0] for v in points]
yy1 = [v[1] for v in points]
plt.plot(xx1,yy1)
plt.plot(xx, yy)
